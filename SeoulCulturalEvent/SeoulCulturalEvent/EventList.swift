import Foundation

struct MainInfo : Codable, Hashable {
    var culturalEventInfo : CulturalEventInfo
}

struct CulturalEventInfo : Codable, Hashable {
    var row : [Row]
}

struct Row : Codable, Hashable {
    var GUNAME : String
    var TITLE : String
    var PLACE : String
    var USE_TRGT : String
    var USE_FEE : String
    var ORG_LINK : String
    var MAIN_IMG : String
    var STRTDATE : String
    var END_DATE : String
}

class CulturalEventData : ObservableObject {
    
    @Published var events : MainInfo
    
    init (events : MainInfo = MainInfo(culturalEventInfo: CulturalEventInfo(row: []))) {
        self.events = events
    }
}
